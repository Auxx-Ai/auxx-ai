// packages/sdk/src/util/generate-app-env-types.ts

import fs from 'fs/promises'
import path from 'path'
import { complete, errored, type Result } from '../errors.js'

/**
 * Marker used to identify env type files that are safe for the CLI to update.
 */
const GENERATED_MARKER = '// Auto-generated by auxx CLI. Do not edit.'

/**
 * Relative output path for app-local SDK env type declarations.
 */
const APP_ENV_TYPES_RELATIVE_PATH = path.join('src', 'auxx-env.d.ts')

/**
 * Canonical content for the generated app env type declarations file.
 */
const APP_ENV_TYPES_FILE_CONTENT = `// src/auxx-env.d.ts
${GENERATED_MARKER}
import '@auxx/sdk/client'
import '@auxx/sdk/global'
`

/**
 * Error type for failures while generating app-local env type declarations.
 */
export type GenerateAppEnvTypesError =
  | { code: 'FAILED_TO_READ_APP_ENV_TYPES_FILE'; path: string }
  | { code: 'FAILED_TO_WRITE_APP_ENV_TYPES_FILE'; path: string }
  | { code: 'FAILED_TO_CREATE_APP_SRC_DIRECTORY'; path: string }

/**
 * Status describing the generator outcome.
 */
export type GenerateAppEnvTypesStatus = 'created' | 'updated' | 'unchanged' | 'skipped_unmanaged'

/**
 * Ensures the app-local env declaration file exists and stays synchronized with the SDK.
 * Existing unmanaged files are preserved and reported as `skipped_unmanaged`.
 *
 * @param rootDirAbsolute Absolute path to the app root. Defaults to current working directory.
 * @returns Result containing a generation status or a filesystem error.
 */
export async function generateAppEnvTypes(
  rootDirAbsolute: string = path.resolve('.')
): Promise<Result<GenerateAppEnvTypesStatus, GenerateAppEnvTypesError>> {
  const srcDirAbsolute = path.join(rootDirAbsolute, 'src')
  const envTypesFilePath = path.join(rootDirAbsolute, APP_ENV_TYPES_RELATIVE_PATH)

  try {
    await fs.mkdir(srcDirAbsolute, { recursive: true })
  } catch {
    return errored({
      code: 'FAILED_TO_CREATE_APP_SRC_DIRECTORY',
      path: srcDirAbsolute,
    })
  }

  let existingContent: string | null = null
  try {
    existingContent = await fs.readFile(envTypesFilePath, 'utf8')
  } catch (error) {
    if (typeof error === 'object' && error !== null && 'code' in error && error.code === 'ENOENT') {
      existingContent = null
    } else {
      return errored({
        code: 'FAILED_TO_READ_APP_ENV_TYPES_FILE',
        path: envTypesFilePath,
      })
    }
  }

  if (existingContent === null) {
    try {
      await fs.writeFile(envTypesFilePath, APP_ENV_TYPES_FILE_CONTENT, 'utf8')
      return complete('created')
    } catch {
      return errored({
        code: 'FAILED_TO_WRITE_APP_ENV_TYPES_FILE',
        path: envTypesFilePath,
      })
    }
  }

  if (existingContent === APP_ENV_TYPES_FILE_CONTENT) {
    return complete('unchanged')
  }

  if (!existingContent.includes(GENERATED_MARKER)) {
    return complete('skipped_unmanaged')
  }

  try {
    await fs.writeFile(envTypesFilePath, APP_ENV_TYPES_FILE_CONTENT, 'utf8')
    return complete('updated')
  } catch {
    return errored({
      code: 'FAILED_TO_WRITE_APP_ENV_TYPES_FILE',
      path: envTypesFilePath,
    })
  }
}
