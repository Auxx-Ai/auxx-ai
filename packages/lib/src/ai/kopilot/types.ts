// packages/lib/src/ai/kopilot/types.ts

/** Routes the supervisor can classify a user message into */
export type KopilotRoute = 'simple' | 'search' | 'multi-step' | 'action' | 'conversational'

/** Execution mode hint from the supervisor */
export type KopilotExecutionMode = 'one-shot' | 'agentic'

/** A single step in a planner-generated plan */
export interface PlanStep {
  id: string
  description: string
  tool?: string
  args?: Record<string, unknown>
  status: 'pending' | 'in-progress' | 'completed' | 'failed'
  result?: string
}

/** Supervisor structured output */
export interface SupervisorClassification {
  route: KopilotRoute
  reasoning: string
  executionMode: KopilotExecutionMode
}

/** Open-ended UI context bag. Known fields provide autocomplete, any key is valid. */
export interface SessionContext extends Record<string, unknown> {
  /** Current page the user is viewing (e.g. 'mail', 'contacts', 'workflows') */
  page?: string
}

/** Domain state carried through the Kopilot pipeline */
export interface KopilotDomainState {
  /** UI context — replaced wholesale on each message via applyContext */
  context: SessionContext
  /** Supervisor classification for the current turn */
  classification?: SupervisorClassification
  /** Plan steps generated by the planner agent */
  plan?: PlanStep[]
  /** Index of the current plan step being executed */
  currentPlanStepIndex?: number
  /** Accumulated tool results for the responder to synthesize */
  toolResults?: Array<{ tool: string; result: unknown }>
}
