/** Zero Width Joiner character used in emoji sequences */
const ZWJ = '\u200D'

/** Variation Selector 16 - forces emoji presentation */
const VS16 = '\uFE0F'

/**
 * Applies a skin tone modifier to an emoji.
 * Inserts skin tone after the base emoji but before any ZWJ or FE0F.
 * For simple emojis, appends the skin tone at the end.
 */
export function applyEmojiSkinTone(emoji: string, skinTone: SkinTone): string {
  if (!skinTone) return emoji

  // Find the first occurrence of ZWJ or VS16 (variation selector)
  const zwjIndex = emoji.indexOf(ZWJ)
  const vs16Index = emoji.indexOf(VS16)

  // Get the earliest modifier position (ignoring -1 values)
  let insertIndex = -1
  if (zwjIndex !== -1 && vs16Index !== -1) {
    insertIndex = Math.min(zwjIndex, vs16Index)
  } else if (zwjIndex !== -1) {
    insertIndex = zwjIndex
  } else if (vs16Index !== -1) {
    insertIndex = vs16Index
  }

  if (insertIndex !== -1) {
    return emoji.slice(0, insertIndex) + skinTone + emoji.slice(insertIndex)
  }

  return emoji + skinTone
}

