'use client'

import type React from 'react'
import { useMemo } from 'react'
import { getVariableDisplayType } from '~/components/workflow/utils/variable-utils'
import type { DisplayVariable, OutputVariablesDisplayProps } from '../../types/output-variables'
import { BaseType, type UnifiedVariable } from '../../types/variable-types'
import Section from '../section'
// import Section from '../section'
import { VarItem } from '../structured-output-generator/output-vars'

/**
 * Converts a UnifiedVariable to a DisplayVariable structure for the VarItem component
 * Recursively processes nested objects and arrays to any depth
 */
function unifiedVariableToDisplayVariable(variable: UnifiedVariable): DisplayVariable {
  const displayVar: DisplayVariable = {
    name: variable.label,
    type: getVariableDisplayType(variable),
    description: variable.description,
  }

  // Handle object types with properties - recursively process each property
  if (variable.type === BaseType.OBJECT && variable.properties) {
    displayVar.subItems = Object.entries(variable.properties).map(([key, prop]) => {
      // Create a child variable with proper label and recursively convert it
      const childVariable: UnifiedVariable = { ...prop, label: key }
      return unifiedVariableToDisplayVariable(childVariable)
    })
  }

  // Handle array types with items - recursively process the items structure
  if (variable.type === BaseType.ARRAY && variable.items) {
    // Create a child variable for the array item and recursively convert it
    // Preserve the original label if it exists (for entity types like "Product")
    const childVariable: UnifiedVariable = {
      ...variable.items,
      label: variable.items.label || '[item]',
    }
    displayVar.subItems = [unifiedVariableToDisplayVariable(childVariable)]
  }

  return displayVar
}

/**
 * Component to display output variables from a node in a read-only format
 */
export const OutputVariablesDisplay: React.FC<OutputVariablesDisplayProps> = ({
  outputVariables = [],
  outputs,
  nodeId,
  title = 'Output Variables',
  description = 'Output variables generated by this node',
  initialOpen = false,
  className,
}) => {
  const variables = outputVariables

  // Transform UnifiedVariables to DisplayVariables
  const displayVariables = useMemo(() => {
    return variables.map(unifiedVariableToDisplayVariable)
  }, [variables])
  if (!variables || variables.length === 0) {
    return null
  }

  return (
    <Section
      title={title}
      initialOpen={initialOpen}
      description={description}
      className={className}>
      {displayVariables.map((variable, index) => (
        <VarItem
          key={index}
          name={variable.name}
          type={variable.type}
          description={variable.description}
          subItems={variable.subItems}
        />
      ))}
    </Section>
  )
}
